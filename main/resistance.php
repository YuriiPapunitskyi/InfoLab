<?php

// Початкові параметри
$rho0 = 1.0e-7;        // Початковий питомий опір, Ом·м (припустимо)
$k = 5;                // Коефіцієнт деформаційної чутливості (умовне значення)
$L0 = 0.01;            // Початкова довжина плівки, м
$A0 = 1.0e-8;          // Початкова площа поперечного перерізу, м²
$nu = 0.3;             // Коефіцієнт Пуассона (для металів ~0.3)

// Функція для розрахунку опору при заданій відносній деформації ε
function calculateResistance($epsilon, $rho0, $k, $L0, $A0, $nu) {
    $rho = $rho0 * (1 + $k * $epsilon);
    $L = $L0 * (1 + $epsilon);
    $A = $A0 * (1 - $nu * $epsilon);  // спрощено
    if ($A <= 0) {
        return NAN; // Некоректне значення (занадто велика деформація)
    }
    return $rho * $L / $A;
}

// Виведення таблиці значень для деформації від 0% до 5%
echo "Epsilon (%)\tResistance (Ohms)\n";
for ($i = 0; $i <= 50; $i += 5) {
    $epsilon = $i / 1000; // Переводимо в частки одиниці (наприклад, 0.005)
    $R = calculateResistance($epsilon, $rho0, $k, $L0, $A0, $nu);
    printf("%.1f\t\t%.6e\n", $epsilon * 100, $R);
}

?>

