<?php

$N = 10000;
$a1 = 0;
$A1 = 2 * sqrt(2);

$S = 0;

for ($j = 0; $j < $N; $j++) {
    $R1 = lcg_value(); // псевдовипадкове число (0,1)
    $x1 = $a1 + ($A1 - $a1) * $R1;

    $a2 = 0.5 * $x1;
    $A2 = 2 * $x1;

    $R2 = lcg_value();
    $x2 = $a2 + ($A2 - $a2) * $R2;

    $phi = sqrt(1 + pow($x1, 2));

    // Додаємо до суми
    $S += $phi;
}

$J = ($A1 - $a1) * $S / $N;

echo "Наближене значення інтеграла J = " . round($J, 6) . "\n";

